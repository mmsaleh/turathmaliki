<p>فإذا أردت ترتيب أفعال حجك فاسمعن بيان ذلك واستجمع ذهنك وأحضره لتكون على بصيرة فيما أذكر لك ذلك أن مريد الإحرام بالحج إذا وصل ميقاته حرم عليه مجاوزته حلالًا فمن كان من أهل المغرب أو الشام أو مصر فإنه يحرم من رابغ لأنه من أعمال الجحفة، فإذا وصله تنظف بحلق الوسط وهو العانة ونتف الإبطين وقص الشارب والأظفار، وأما حلق الرأس فيندب تركه طلبًا للشعث في الحج، ثم يغتسل ولو كان حائضًا أو نفساء، صغيرًا أو كبيراً وإن كان جنباً اغتسل للجنابة والإحرام غسلًا واحدًا، وكذلك إذا طهرت الحائض ويتدلك في هذا الغسل ويزيل الوسخ بخلاف ما بعده من الاغتسالات الآتية في صفة الحج فليس فيها إلا إمرار اليد مع الماء وإلى صفة هذا الغسل أشار بقوله (كواجب) أي مثل غسل واجب، ويكون هذا الاغتسال متصلًا بالإحرام كغسل الجمعة بصلاتها، فإذا اغتسل لبس إزارًا ورداء ونعلين، ولو ارتدى بثوب واحد جاز ثم يستصحب هديًا ثم يصلي ركعتين يقرأ فيهما مع الفاتحة الكافرون والإخلاص ويدعو إثرهما، ثم يركب راحلته، فإذا استوى عليها أحرم وإن كان راجلًا أحرم حين يشرع في المشي، والإحرام هو الدخول بالنية في أحد النسكين مع قول يتعلق بالإحرام كالتلبية والتكبير، والتلبية أن يقول: لبيك اللهم لبيك لبيك لا شريك لك لبيك إن الحمد والنعمة لك والملك لا شريك لك، ومعنى لبيك إجابة لك بعد إجابة، ويستحضر عند التلبية أنه يجيب مولاه فلا يضحك ولا يلعب، ويجدد التلبية عند تغيير الأحوال كالقيام والقعود والنزول والركوب والصعود والهبوط وعند ملاقاة الرفاق ودبر الصلوات سواء كانت نوافل أو فرائض، ويتوسط في علو صوته وفي ذكرها فلا يلح بها بحيث لا يفتر ولا يسكت ولا يزال كذلك محرماً يلبي حتى يقرب من مكة، فإذا قرب منها فالحكم كما يذكره الناظم في قوله:</p> 
