			<p>سنن الغسل أربع:</p>
			<p>الأولى: المضمضة مرة واحدة.</p>
			<p>الثانية: غسل اليدين إلى الكوعين مرة واحدة وذلك ابتداء غسله قبل إدخالهما في الإناء.</p>
			<p> الثالثة: الاستنشاق مرة واحدة.</p>
			<p>الرابعة: مسح ثقب الأذنين. وأما جلدة الأذنين فلا خلاف في وجوب غسلها. ثم قال:</p> 
