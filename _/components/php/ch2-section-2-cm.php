<p>سنن الصلاة اثنتان وعشرون سنة:</p>
<p> الأولى: قراءة السورة بعد قراءة الفاتحة، وعن الفاتحة عبر بالواقية لأنها من أسمائها، وذلك في الركعة الأولى والثانية من سائر الفرائض، وذلك للإمام والمنفرد.</p>
<p>الثانية: القيام لقراءة السورة في الركعة الأولى والثانية. وذلك للإمام والمنفرد.</p>
<p>الثالثة والرابعة: الجهر بمحله والسر بمحله فمحل الجهر الصبح والجمعة وأولتا المغرب والعشاء. ومحل السر الظهر والعصر وآخرة المغرب وآخرتا العشاء.</p>
<p> والخامسة: التكبيرة إلا تكبيرة الإحرام فإنها فرض كما تقدم وكل تكبير سنة،</p>
<p>والسادسة والسابعة: التشهد الأول والثاني.</p>
<p>الثامنة والتاسعة: الجلوس الأول والجلوس الثاني إلا القدر الذي يقع فيه السلام فإنه فرض كما تقدم في الفرائض.</p>
<p> والعاشرة: سمع الله لمن حمده في الرفع من الركوع للإمام والمنفرد، وهذه السنن من قراءة السورة إلى هنا من السنن المؤكدة التي يسجد المصلي لتركها إلا التكبيرة والتسميع فلا يسجد لهما المصلي إلا إذا تعددتا. وهذا معنى قول الناظم: (هكذا أكدا)، والباقي كالمندوب أي والباقي من السنن فغير متأكد وحكم من تركها كمن ترك مندوبًا لا شيء عليه.</p>
<p>الحادية عشرة: إقامة الصلاة وهي سنة لكل فرض وقتيًا كان أو فائتًا، وهذا للرجل. وأما المرأة فإن أقامت سرًا فحسن وتصح صلاتها ولو تركت الإقامة عمدًا.</p>
<p> الثانية عشرة: السجود على اليدين والركبتين وأطراف الرجلين.</p>
<p>الثالثة عشرة: إنصات المقتدي أي سكوت المأموم لقراءة الإمام في الصلاة الجهرية. </p>
<p>الرابعة عشرة: رد المأموم السلام على الإمام ويرد ولو كان مسبوقًا فلم يسلم حتى ذهب إمامه ويرد قبالته.</p>
<p> الخامسة عشرة: رد المأموم السلام على من على يساره إن كان وإلا فلا.</p>
<p> السادسة عشرة: المكث الزائد على أقل ما يقع عليه اسم الطمأنينة التي هي سكون الأعضاء. قوله: ((وزائد سكون))، أي السكون الزائد على القدر الواجب منه.</p>
<p> السابعة عشرة: السترة للإمام والمنفرد إذا خافا المرور بين أيديهما فإن لم يخافا صليا بدون سترة.</p>
<p>الثامنة عشرة: الجهر بالسلام الذي يخرج به المصلي من الصلاة.</p>
<p>التاسعة عشرة: لفظ التشهد الذي هو التحيات لله الزاكيات لله الطيبات الصلوات لله السلام عليك أيها النبي ورحمة الله وبركاته السلام علينا وعلى عباد الله وحده لا شريك له. أشهد أن لا إله إلا الله وحده لا شريك له، وأشهد أن محمداً عبده ورسوله.</p>
