<p>الصلاة تبطل على المقتدي وهو المأموم بما تبطل به على إمامه، بمعنى أنه إذا بطات صلاة الإمام سرى البطلان لصلاة المأموم فتبطل أيضاً لارتباط صلاته بصلاة إمامه إلا في فرعين ذكر الحدث أو غلبته على ما اقتصر عليه الناظم فإذا تذكر الإمام الحدث أو غلبه وبادر بالخروج من الصلاة صحت صلاة المأموم وإن لم يبادر الإمام بالخروج فإنها تبطل على المأمومين أيضاً لاقتدائهم بمحدث متعمد لذلك. ثم إن الإمام يستحب له أن يقدم مؤتماً من مأموميه يتم الصلاة بمعنى أنه يستخلفه على بقية الصلاة، فإن أبى وامتنع الإمام من ذلك فذهب ولم يستخلف عليهم أحدًا فهم مخيرون بين أن ينفردوا أي يتموا الصلاة أفذاذًا في غير الجمعة وبين أن يقدموا أي يستخلفوا واحدًا منهم يكمل بهم الصلاة فهم من قوله تقديم مؤتم أنه لا يستخلف من ليس من مأموميه، وكذا من دخل معه بعد حصول العذر لأنه أجنبي.</p>
