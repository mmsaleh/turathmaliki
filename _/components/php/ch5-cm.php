<p>فإذا تحقق غروب الشمس يوم عرفة دفع الإمام ودفع الناس معه إلى المزدلفة بسكينة ووقار، فإذا وجد فرجة حرك دابته ويمر بين المأزمين وهما الجبلين اللذان يمر الناس بينهما إلى المزدلفة، ويذكر الله في طريقه، ويؤخر صلاة المغرب إلى أن يصل للمزدلفة، فإذا وصلها صلى المغرب والعشاء جمعاً ويقصر العشاء، ولكل صلاة أذان وإقامة، ويصليهما إن تيسر له مع الإمام وإلا ففي رحله ويبدأ بالصلاة حين وصوله ولا يتعشى إلا بعد الصلاتين إلا أن يكون عشاء خفيفاً والنزول بالمزدلفة واجب والمبيت فيها إلى الفجر سنة، فإن لم ينزل فعليه الدم ويستحب إحياء هذه الليلة بالعبادة، ويستحب أن يصلي بها الصبح أول وقته، فإذا صلاه وقف بالمشعر الحرام مستقبل القبلة والمشعر عن يساره يكبر ويدعو للاسفار، ثم يلتقط سبع حصيات لجمرة العقبة من المزدلفة، أما بقية الجمار فيلتقطها من أين شاء ثم يدفع قرب الأسفار إلى منى، ويحرك دابته ببطن محسر، وقد قدر رمية بحجر، ويسرع الماشي في مشيه، فإذا وصل إلى منى أتى بجمرة العقبة على هيئته من ركوب أو مشي، فإذا وصلها رماها بسبع حصيات متواليات يكبر مع كل حصاة ويرميها، ويحصل التحلل الأول وهو التحلل الأصغر يحل له كل شيء مما يحرم عليه كما يأتي إلا النساء والصيد، ويكره الطيب، ثم يرجع إلى منى فينزل حيث أحب وينحر هديه إن أوقفه بعرفة وإن لم يقف به بعرفة نحره بمكة بعد أن يدخل به من الحل ثم يحلق جميع شعر رأسه وهو الأفضل ويجزئه التقصير وهو السنة للمرأة، ثم يأتي مكة فيطوف طواف الإفاضة في ثوبي إحرامه استحباباً، ثم يصلي ركعتين، ثم يسعى بين الصفا والمروة سبعة أشواط كما تقدم إن لم يكن سعى بعد طواف القدوم، فإن كان قد سعى لم يعده، وبهذا يحصل التحلل الأكبر فيحل له ما بقي وهو النساء والصيد والطيب ويدخل وقت طواف الإفاضة بطلوع الفجر من يوم النحر، فإذا طاف للإفاضة وسعى بعده إن كان لم يسع قبل ذلك فإنه يرجع إلى منى ويقيم بها بقية يوم النحر وثلاثة أيام بعده لرمي الجمار وعلى ذلك نبه بقوله:</p>
<table class="table table-bordered table-striped table-hover">
	<tr>
		<td>268</td>
		<td>وَارْجِعْ فَصَلِّ الظُّهْرَ في مِنَى وَبِتْ</td>
