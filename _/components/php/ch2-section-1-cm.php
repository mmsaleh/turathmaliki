<p>شروط أداء الصلاة أربعة:</p>
<p>الأول: استقبال القبلة وهو شرط ابتداءً ودوامًا مع الذكر والقدرة دون العجز والنسيان فمن صلى لغير القبلة عامدًا قادرًا على استقبالها فصلاته باطلة.</p>
<p> الشرط الثاني: طهارة الخبث وهو النجس، فإزالة النجاسة عن الثوب والبدن والمكان شرط ابتداءً ودوامًا مع الذكر والقدرة دون العجز والنسيان.</p>
<p> الشرط الثالث: ستر العورة وهو أيضًا شرط مع الذكر والقدرة ساقط مع العجز والنسيان. </p>
<p>الرابع: طهارة الحدث وهو أيضًا شرط ابتداءً ودوامًا فمن افتتح الصلاة متطهرًا ثم أحدث فيها بطلت صلاته كمن افتتحها محدثًا ولا فرق في البطلان بين العمد والنسيان ولا بين العجز والاختيار، ولهذا قال الناظم: (في غير الأخير) ثم إن فروع ناسي الشروط المذكورة والعاجز عنها كثيرة فالناسي لأحد الشروط الثلاثة الأول أو العاجز عنه إذا صلى غير محصل له فتذكر أو زال عجزه فإنه يستحب له أن يعيد في الوقت إلا لعاجز عن استقبال القبلة أو عن ستر العورة فالإعادة عليهما فضمير عجزها، للقبلة والمراد بالغطاء ستر العورة. ثم قال: </p>
