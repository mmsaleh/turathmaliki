			<p>مستحبات الغسل سبعة:</p> 
			<p>الأول: أن يبدأ بغسل ما بفرجه أو جسده من أذى بعد غسل يديه أولًا على وجه السنية،</p> 
			<p>الثاني: التسمية،</p>
			<p>الثالث: أن يفيض الماء على رأسه ثلاث غرفات، والغرفة ملء اليدين جميعًا، وهذا بعد أن يخلل شعر رأسه ببلل أصابعه.</p>
			<p>الرابع: تقديم أعضاء الوضوء لشرفها وبغسلها بنية الحدث الأكبر، وكذلك بغسلها مرة .</p>
			<p>الخامس: قلة الماء من غير تحديد في ذلك.</p>
			<p>السادس: البدء بأعلى البدن قبل أسفله.</p>
			<p>والسابع: البدء بالميامن قبل المياسر. ثم قال:</p>
