			<p>يباح التيمم لخوف حدوث المرض باستعمال الماء أو زيادة المرض أو تأخير البرء أو ذهاب العرق وخاف إن قلع جف عرقه  ودامت علته، وكذا لفقد الماء الكافي للوضوء أو الغسل بالسفر، أو فقد القدرة على استعمال الماء لعجز أو ربط أو إكراه، أو  خاف خروج الوقت باستعماله، أو فقد من يناوله الماء، وكذا يتيمم من عنده ماء إن توضأ به خاف العطش سواء خاف الموت أو  الضرر، وكذا إذا ظن عطش من معه من آدمي أو دابة، وكذا يتيمم من خاف على نفسه من لصوص أو سباع، وكذا من خاف  على تلف مال له بال. ثم قال:</p>
