<p>مندوبات التيمم تسعة: وهي التسمية والصمت إلا عن ذكر الله والاستقبال وتقديم اليمنى وجعل ظاهرها من طرف باطن اليسرى إلى المرفق ثم باطنها إلى آخر الأصابع واليسرى كذلك، والتيمم على تراب غير منقول، والبدء بأعلى الوجه وبأطراف الأصابع،  ثم قال:</p>
