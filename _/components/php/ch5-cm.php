<p>أخبر أن من دنت منه مكة وقربت فوصل إلى ذي طوى، أو ما كان على قدر مسافتها اغتسل أيضًا لدخول مكة بصب الماء مع إمرار اليد بلا تدلك، ثم يدخل مكة من كداء الثنية التي بأعلى مكة ولا يزال يلبي حتى يصل لبيوت مكة، فإذا وصلها ترك التلبية وكل شغل، ويقصد المسجد لطواف القدوم، ويستحضر ما أمكنه من الخضوع والخشوع ولا يركع تحيةو المسجد بل يقصد الحجر الأسود وينوي طواف القدوم أو طواف العمرة إن كان فيها فيقبله بفيه ثم يكبر، فإن زوحم عن تقبيله لمسه بيده ثم وضعها على فيه من غير تقبيل ثم يكبر، فإن لم تصل يده فبعود إن كان لا يؤذي به أحدًا وإلا ترك وكبر ومضى ولا يدع التكبير استلم أم لا، ثم يشرع في الطواف فيطوف والبيت عن يساره سبعة أشواط أي أطواف وعلى ذلك نبه الناظم بقوله سبعة أشواط به وقد يسر أي بالبيت أي والحالة أنك قد يسرته أي جعلته لناحية اليسار، فإذا وصل إلى الركن اليماني لمسه بيده ثم وضعها على فيه من غير تقبيل وكبر، فإن لم يقدر كبر ومضى، فإذا دار بالبيت حتى وصل الحجر الأسود فذلك شوط، وكلما مر به أو بالركن اليماني فعل بكل واحد منهما كما ذكرنا فيه إلى آخر الشوط السابع، ويستحب للرجل أن يرمل في الأشواط الثلاثة الأولى من هذا الطواف ويمشي في الأربعة بعدها، والرمل فوق المشي ودون الجري ولا ترمل المرأة لا في طواف القدوم ولا في غيره، ولا يرمل الرجل في غير طواف القدوم، فإذا فرغ من الطواف صلى ركعتين خلف مقام سيدنا إبراهيم ( بالكافرون والإخلاص، ويستحب الدعاء بعد الطواف بالملتزم وهو ما بين الباب والحجر الأسود، فإذا فرغ قبل الحجر الأسود ثم يخرج إلى الصفا بقصد السعي، وعلى ذلك نبه بقوله:</p>
