<p>الجماعة واجبة في الجمعة وسنة في غيرها من سائر الفرائض بمعنى أن إيقاع صلاة الجمعة في الجماعة واجب وإيقاع غيرها من سائر الفرائض في الجماعة سنة ومعنى قوله (وبركعة رست) أي ثبت فضل الجماعة وحصل بإدراك ركعة فأكثر فمن أدرك ركعة فأكثر من صلاة الجماعة فقد أدرك فضلها الذي يحصل لمن حضرها من أولاها إذا كان قد فاته ذلك اضطرارًا لا مختارًا، وأما إذا كان مختارًا فلا يحصل له ذلك ومعنى قوله (وندبت إعادة الفذ بها) أن من صلى فذًّا، أي وحده يستحب له أن يعيد في الجماعة إلا المغرب إذا صلاها وحده فلا يعيدها في جماعة، وكذا العشاء إن أوتر بعدها، وأما إن صلّى العشاء وحده ولم يوتر فيستحب له إعادتها مع جماعة. ثم قال: </p>
