<p>أمر من فرغ من الطواف وقبل الحجر الأسود أن يخرج إلى الصفا، فإذا وصل إليها رقي عليها فيقف مستقبل القبلة ثم يقول: الله أكبر ثلاثاً، لا إله إلا الله وحده لا شريك له، له الملك وله الحمد وهو على كل شيء قدير لا إله إلا الله وحده أنجز وعده ونصر عبده وهزم الأحزاب وحده، ثم يدعو ويصلي على النبي ( ، ثم ينزل ويمشي ويخب في بطن المسيل أي يسرع إسراعاً شديداً، فإذا جاوزه مشى حتى يبلغ المروة، فذلك شوط، فإذا وصل المروة رقى عليها ويفعل كما تقدم في الصفا ثم ينزل ويفعل كما وصفنا من الذكر والدعاء والصلاة على النبي ( والخبب، فإذا وصل إلى الصفا فذلك شوط ثان، وهكذا حتى يكمل سبعة يكمل سبعة أشواط بعد الذهاب للمروة شوطًا والرجوع منها للصفا شوطاً آخر فيقف أربع وقفات على الصفا وأربعاً على المروة يبدأ بالصفا ويختم بالمروة. ثم قال:</p>
