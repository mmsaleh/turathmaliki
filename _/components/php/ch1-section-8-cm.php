			<p>من تيمم للفرض لا يجوز له أن يصلي بالتيمم إلا فرضًا واحدًا ولا يجوز له أن يصلي بالتيمم فرضين ولو قصدهما به، فإن  الفرض الثاني باطل ولو مشتركي الوقت كالظهر والعصر مثلاً، وجاز له أن يصلي بذلك التيمم على الجنازة وأن يصلي به سنة  غير صلاة الجنازة كالوتر لمن تيمم للعشاء وصلاها إذا كان ذلك متصلًا بالفرض الذي تيمم له، وأما من تيمم لنافلة أو لقراءة في  مصحف ثم صلى فريضة بذلك التيمم فإن صلاته باطلة. ثم قال:</p>
