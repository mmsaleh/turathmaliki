<p>فرائض الصلاة ست عشرة:</p> 
<p>أولاها: تكبيرة الإحرام، أي التكبيرة التي يدخل بها المصلي في حرمة الصلاة وهي واجبة على الإمام المنفرد والمأموم. ولفظها: الله أكبر.</p>
<p> الثانية: القيام لتكبيرة الإحرام.</p>
<p>الثالثة: نية الصلاة المعينة بكونها ظهرًا أو عصرًا أو مغربًا أو عشاء أو فجرًا،</p>
<p> الرابعة: قراءة الفاتحة: وهي واجبة على الإمام والمنفرد دون المأموم،</p>
<p>الخامسة: القيام لقراءة الفاتحة،</p>
<p>السادسة: الركوع،</p>
<p>السابعة: الرفع من الركوع،</p>
<p> الثامنة: السجود،</p>
<p>التاسعة: الرفع من السجود،</p>
<p> العاشرة: السلام بلفظ السلام عليكم،</p> 
<p>الحادية عشرة: الجلوس للسلام بقدر ما يقع فيه السلام،</p>
<p> الثانية عشرة: ترتيب أداء الصلاة بحيث يقدم القيام على الركوع والركوع على السجود والسجود على الجلوس،</p> 
<p>الثالثة عشرة: الاعتدال وهو نصب القامة،</p>
<p> الرابعة عشرة:  الطمأنينة وهي سكون الأعضاء في جميع أركان الصلاة زمنًا ما،</p>
<p>الخامسة عشرة: متابعة المأموم للإمام في الإحرام والسلام بمعنى أنه لا يحرم إلا بعد أن يحرم إمامه ولا يسلم إلا بعد سلامه،</p>
<p>السادسة عشرة: نية الاقتداء، وهي واجبة على المأموم في جميع الصلوات فيجب على المأموم أن ينوي أنه مقتد بالإمام متبع له، فإن لم ينوه بطلت صلاته، ثم قال:</p>
