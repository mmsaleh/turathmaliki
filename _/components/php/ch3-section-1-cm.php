<p>لا فرق في زكاة العين بين كون النصاب كله ذهبًا أو كله فضة وبين كونه ملفقًا منهما لكن بالتجزئة والمقابلة بأن يجعل كل دينار في مقابلة عشرة دراهم شرعية وافق ذلك صرف الوقت أم لا، فمن له مائة وثلاثون درهمًا ودينار يساوي عشرين درهمًا لا زكاة عليه، وكذلك في زكاة الماشية لا فرق بين كون النصاب الغنم كله ضأنًا أو كله معزًا أو ملفقًا منهما كعشرين من كل منهما أو نصاب البقر كله بقرًا أو كله جواميس أو ملفقًا منهما أو نصاب الإبل كله إبلًا أي عرابًا أو كله بختًا أو ملفقًا منهما، وكذلك في زكاة الحرث لا فرق بين كون النصاب كله قمحًا أو شعيرًا أو سلتًا وبين كونه ملفقًا من اثنين منهما أو ثلاثة وكذلك لا فرق بين كون النصاب من نوع واحد من القطاني أو من نوعين أو أكثر من أنواعها كخمسة أوسق بين فول وعدس وحمص فيضم بعضها لبعض وتزكى، وكذلك لا فرق بين كون نصاب الزبيب كله أحمر أو كله أسود أو ملفقًا منهما، ولا بين كون نصاب التمر كله صنفًا واحدًا أو ملفقًا من صنفين أو أكثر. 
