			<p>الأول والثاني: انقطاع دم الحيض والنفاس.</p> 
			<p>والثالث: الإنزال، وهو خروج المني المقارن للذة المعتادة،</p>
			<p>والرابع: مغيب الحشفة وتسمى الكمرة، وهي رأس الذكر في فرج آدمي أو غيره، أو ذكر حي أو ميت بإنعاظ أم لا أنزل أم لا  في قبل أو دبر، وإلى هذا التعميم في مغيب الحشفة أشار الناظم بقوله إسجال لأنه مصدر أسجل إذا أطلق أرسل ولم يقيد، ثم قال:</p>
