<p>من طاف للقدوم وسعى ينبغي له أن يذهب ثامن الحجة ويسمى يوم التروية إلى منى ملبياً بقدر ما يدرك به صلاة الظهر أي آخر وقته المختار، ويكره قبل ذلك أو بعده إلا لعذر وينزل بها بقية يومه وليلته ويصلي بها الظهر زالعصر والمغرب والعشاء والصبح كل صلاة في وقتها ويقصر الرباعية. والسنة أن لا يخرج الناس من منى يوم عرفة حتى تطلع الشمس، فإذا طلعت ذهبوا إلى عرفة وينزلون بنمرة. فإذا قرب الزوال فليغتسل كغسل دخوله مكة، فإذا زالت الشمس فليرح إلى مسجد نمرة ويقطع التلبية ثم يخطب الإمام بعد الزوال خطبتين يجلس بينهما يعلم الناس فيهما ما يفعلون إلى ثاني يوم النحر، ثم يصلي بالناس الظهر والعصر جمعاً وقصرًا لكل صلاة أذان وإقامة، ومن لم يحضر صلاة الإمام جمع وقصر في رحله ولو ترك الحضور من غير عذر، يدفع الإمام والناس إلى موقف عرفة، وعرفة كلها موقف، وحيث يقف الإمام أفضل، والوقوف راكبًا أفضل، لفعله ( إلا أن يكون بدابته عذر، والقيام أفضل من الجلوس ولا يجلس إلا لتعب، وتجلس المرأة، ووقوفه طاهرًا متوضئًا مستقبل القبلة أفضل ويكثر من قوله لا إله إلا الله وحده لا شريك له له الملك وله الحمد وهو على كل شيء قدير، ولا يزال كذلك مستقبل القبلة بالخشوع والتواضع وكثرة الذكر والدعاء والصلاة على النبي ( إلى أن يتحقق غروب الشمس إذ الوقوف الركني هو الكون في عرفة في جزء من ليلة النحر، فإذا بقي بها حتى تحقق الغروب فقد حصل القدر الواجب من الوقوف وإلى الوقوف بعرفة وكيفيته ووقته أشار بقوله: ثم الجبل اصعد إلى قوله هنيهة بعد غروبها تقف. ثم بعد الغروب ينفرون إلى مزدلفة وعلى ذلك نبه بقوله:</p>
