	</tr>
</table>
<p>شروط الكفارة خمسة: التعمد والانتهاك وكونه في رمضان والاختيار والعلم بحرمة فعله، والمعنى أن الكفارة تجب على تعمد في رمضان دون غيره الأكل أو الشرب بفم مع كونه مختارًا غير مضطر لذلك، وسواء وصل إلى جوفه أو إلى حلقه أو تعمد إخراج مني بجماع أو مقدماته ولو بأضعفها وهو الفكر الذي حركه النفس في محاسن من يشتهي للوقاع، أو تعمد رفض ما بني عليه الصوم وهو النية حال كون تعمده خاليًا عن التأويل القريب وعن الجهل، والتأويل القريب هو ما استند صاحبه إلى سبب موجود، والتأويل البعيد هو ما استند صاحبه على سبب معدوم غالبًا ومن أمثلة التأويل القريب، كمن أفطر ناسيًا، أو من طهرت من الحيض قبل الفجر ولم تغتسل إلا بعد طلوع الفجر، أو من تسحر في الفجر أو سافر دون مسافة القصر فظن إباحة الفطر فبيته وأصبح فيه مفطرًا لو رأى هلال شوال يوم الثلاثين نهارًا فظن أنه لليلة الماضية فظن كل واحد منهم أن الفطر مباح له فأفطر فلا كفارة على واحد منهم، ولكن عليهم الإثم إذ لا يحل للإنسان أن يفعل شيئاً حتى يعلم حكم الله فيه. ومن أمثلة التأويل البعيد كمن رأى الهلال ولم تقبل شهادته فأفطر، ومن أفطر لحمى تأتيه أو لحيض عادتها أن يأتيها في مثل ذلك اليوم سواء أتى ذلك أو لم يأت، أو أفطر لسماعه حديث ((أفطر الحاجم والمحتجم)) أو كون (( المغتاب لا صيام له)) فتأويل هؤلاء كالعدم وتجب الكفارة على كل واحد منهم مع القضاء وأما الجاهل الذي لا كفارة عليه فهو من كان حديث عهد بالإسلام فظن أن الفطر إنما هو بالأكل والشرب دون الجماع فجامع فلا يجب عليه إلا القضاء فقط، وفهم من قول الناظم ((في رمضان)) أنه لا كفارة على من أفطر في غير رمضان كان فطره عمدًا أو نسيانًا ولو في قضاء رمضان. وفهم من قوله (إن عمد) أن من أفطر في رمضان نسيانًا فلا كفارة عليه، وفهم من قوله (فم) أن من تعمد في رمضان إدخال شيء من أنفه أو أذنه مثلًا فلا كفارة عليه, وفهم من قوله (أو للمني) أن من خرج منه المني في رمضان من غير تسبب في إخراجه فلا كفارة عليه بل ولا قضاء. ثم قال:</p>
<table class="table table-bordered table-striped table-hover">
	<tr>
		<td>225</td>
		<td>بِلَا تَأَوُّلٍ قَرِيبٍ وَيُبَاحْ</td>
		<td>لِلضُّرِّ أَوْ سَفَرِ قَصْرٍ أَيْ مُبَاحْ</td>
	</tr>
</table>
<p>يباح الفطر ويجوز لأحد أمرين إما لضرر يلحقه بسبب الصيام أو لما هو مظنة الضرر إن لم يحصل الضرر وهو السفر الذي تقصر فيه الصلاة، وهو السفر الطويل المباح، أما إباحة الفطر لضرر فمحله إذا خاف تمادي ضره بقول طبيب أمين أو تجربة في نفسه أو خاف زيادته أو حدوث مرض آخر، أو خاف المشقة لضعفه بالمرض، فإن كان لو تكلفه لقدر عليه فيفطر زدين الله يسر، أما لو خاف التلف أو الأذى الشديد إن صام فإن الصوم يحرم عليه حينئذ ويجب عليه الفطر لأن حفظ النفوس واجب ما أمكن، وأما إباحة الفطر للسفر فله شروط ثلاثة: أحدها كون السفر مما تقصر فيه الصلاة لإباحته وطوله وكون مسافته مقصودة دفعة واحدة، وأن يشرع في السفر قبل الفجر، فإن طلع الفجر قبل أن يشرع فيه فلا يفطر قبل الشروع ولا بعده في ذلك اليوم إن شرع بعد الفجر إلا لضرورة، فإن أفطر قبل خروجه كفر وإن أفطر بعد خروجه فالقضاء فقط، فإن شرع فيه قبل الفجر فله أن يفطر. الثالث: أن لا يبيت الصيام في سفره فإن بيته ثم أفطر لغير عذر فالقضاء والكفارة، وعلة ذلك أنه كان في سعة أن يفطر أو يصوم، فلما صام لم يمكن أن يخرج منه إلا لعذر. ثم قال:</p>
<table class="table table-bordered table-striped table-hover">
	<tr>
		<td>226</td>
		<td>وَعَمْدُهُ في النَّفْلِ دُونَ ضُرِّ</td>
		<td>مُحَرَّمٌ وَلْيَقْضِ لَا في الْغَيْرِ</td>
	</tr>
</table>
<p>يحرم تعمد الفطر في النفل من الصوم لغير ضرر يلحق الصائم، وصيام النفل إحدى المسائل السبع التي تلزم بالشروع فيها عند المالكية ويحرم قطعها ويجب فيها القضاء ولا يجوز له الفطر، ولو حلف الإنسان بالله أو بالطلاق ولو بتًّا فلا يفطر ويحنثه، لكن استثنوا من ذلك الأب والأم إذا عزما عليه فإنه يفطر، وإن لم يحلفا إذا كان منها شفقة عليه لإدامة صومه ونحوه وبعد ذلك يقضي. وأما إذا كان الفطر في التطوع نسياناً أو عمداً لضرر فلا قضاء عليه وهو المفاد بقوله (لا في الغير) أي لا يقضي في غير ما ذكره وهو النسيان والعمد لضرورة. ثم قال:</p>
<table class="table table-bordered table-striped table-hover">
	<tr>
		<td>227</td>
		<td>وَكَفِّرَنْ بِصَوْمِ شَهْرَيْنِ وِلَا</td> 
		<td>أَوْ عِتْقِ مَمْلُوكٍ بالِاسْلَامِ حَلَا</td>
	</tr>
	<tr>
		<td>228</td>
		<td>وَفَضَّلُوا إطْعَامَ سِتِّينَ فَقِيرْ</td>
		<td>مُدّاً لِمِسْكِينٍ مِنَ الْعَيْشِ الْكَثِيرْ</td>
	</tr>
</table>
<p>من وجبت عليه الكفارة بوجه من الوجوه المذكورة سابقًا فعليه أن يكفر بأحد ثلاثة أشياء إما بصوم شهرين متواليين أي متتابعين، وإما بعتق مملوك مسلم، وإما بإطعام ستين مسكينًا مدًا لكل مسكين بمده ( من غالب عيش أهل ذلك الموضع، وهذا الوجه الثالث أفضل لأنه أشد نفعاً لتعديه إلا أن يكون خليفة فبالصوم.</p>
