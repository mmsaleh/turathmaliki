<p>تجوز إمامة العنين وهو الذي له ذكر صغير لا يتأتى به الجماع، وقيل هو الذي لا ينتشر ذكره، وتجوز إمامة الأعمى مع وجود غيره إن كان أفقه منه، وتجوز إمامة الألكن وهو الذي لا يستطيع إخراج بعض الحروف من مخارجها سواء كان لا ينطق بالحروف البتة أو ينطق به مغيرًا ولو بزيادة أو تكرار، ويشمل التمتام وهو الذي ينطق أول كلامه بتاء مكررة، والأرت وهو الذي يجعل اللام فاء أو من يدغم حرفًا في حرف، والطمطام وهو الذي يشبه كلامه كلام العجم، والغمغام وهو الذي لا يكاد صوته ينقطع بالحروف والفأفاء، وهو الذي يكرر الفاء والأخن وهو الذي يشوب صوت خياشيمه شيء من الحلق، والأغن وهو الذي يشوب صوته شيء من الخياشيم، والأعجم وهو الذي لا يفرق بين الضاد والظاء وغير ذلك، والألثغ وهو الذي لا يتأتى له النطق ببعض الحروف، وكذا المجذم الخفيف الجذام، وهؤلاء تجوز إمامتهم مع فقد من سلم من ذلك إن كانوا عدولًا قوله (وهذا الممكن) أي وهذا الذي ذكرنا من شروط الإمامة وأحكام صلاة الجماعة هو القدر الممكن اللائق بمثل هذا النظم الموضوع للمبتدئ. ثم قال: </p>
