	<p>سنن الوضوء سبع: </p>
	<p>الأولى: غسل اليدين إلى الكوعين قبل إدخالهما في الإناء إن أمكن الإفراغ وإلا أدخلهما فيه كالماء الكثير والجاري.</p>
	<p>الثانية: رد  مسح الرأس من منتهى المسح لمبدئه.</p>
	<p> الثالثة: مسح الأذنين ظاهرهما وباطنهما مع تجديد الماء لهما.</p>
	<p>الرابعة: المضمضة وهي إدخال الماء في الفم وخضخضته من شدق إلى شدق.</p> 
	<p>الخامسة والسادسة: الاستنشاق والاستنثار بجعل السبابة والإبهام من اليد اليسرى على أنفه.</p>
	<p>السابعة: ترتيب الفرائض فلو نكس ناسيًا أعاد المنكس وحده إن بعد الزمان وإلا أعاده وأعاد ما بعده. ثم قال:</p>
