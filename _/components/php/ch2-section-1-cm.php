<p>شروط وجوب الصلاة، النقاء من دم الحيض والنفاس ويحصل النقاء بالقصة، وهي ماء أبيض كالجير، ويحصل أيضًا بالجفوف وهو خروج الخرقة جافة ليس عليها شيء من دم ولا صفرة ولا كدرة ولا قضاء على الحائض والنفساء للصلاة التي فاتتها أيام الدم بخلاف الصوم فيجب عليها قضاؤه، ومن شروطها دخول أوقات الصلاة يقينًا. ومن شك في دخول الوقت لم تجزه تلك الصلاة ولو وقعت فيه ومعنى قوله (فأدها به حتمًا أقول) أي أن الصلاة في الوقت المختار أداء حتمًا بحيث لا يباح لك تأخيرها عنه إلى الضروري لغير عذر وإلا أثمت وإن كنت مؤديًا لها، ثم قال: </p>
