<p>سنن التيمم ثلاث:</p> 
<p>الأولى: مسح اليدين من الكوعين إلى المرفقين، وأما مسحهما إلى الكوعين ففرض كما تقدم.</p>
<p> والثانية: الضربة الثانية لمسح اليدين،</p> 
<p>والثالثة: الترتيب فيقدم مسح الوجه على مسح اليدين. ثم قال:</p>
