<p>فضائل الوضوء أي مستحباته إحدى عشرة:</p>
			<p>الفضيلة الأولى: التسمية وهي أن يقول أول الوضوء: بسم الله الرحمن الرحيم.</p>
			<p> الثانية: أن يتوضأ في موضع طاهر.</p>
			 <p>الثالثة: أن يقلل الماء من غير تحديد.</p>
			<p>الرابعة: أن يجعل الإناء الذي فيه الماء عن يمينه بخلاف ما إذا كان أعسر.</p>
			<p> الخامسة: الغسلة الثانية والثالثة بمعنى أن تكرار المغسول ثلاثًا مستحب.</p>
			<p> السادسة: البداءة بالميامن من قبل المياسر.</p>
			<p> السابعة: السواك بعود الأراك وإن لم يجد فبالإصبع.</p>
			<p> الثامنة: ترتيب السنن فيما بينها فيقدم غسل اليدين على المضمضة، والمضمضة على الاستنشاق.</p>
			<p> التاسعة: ترتيب السنن مع الواجبات فيقدم غسل اليدين والمضمضة و الاستنشاق والاستنثار على غسل الوجه، ويقدم مسحالأذنين على غسل الرجلين، ويؤخرها عن مسح الرأس.</p> <p>العاشرة: أن يبدأ في مسح رأسه من مقدمه.</p>
			<p> الحادية عشرة: تخليل أصابع الرجلين، ثم قال:</p>
