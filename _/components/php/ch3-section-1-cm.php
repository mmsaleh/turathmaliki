	<p>الأول والثاني: الفقير والمسكين، فالفقير من له شيء من الدنيا لا يكفيه لعيش عامه، والمسكين الذي لا شيء له، ويشترط في كل منهما أربعة شروط: الحرية والإسلام وأن تكون نفقتهما غير واجبة على مليء.</p>
	<p>الثالث: الغازي وهو من يجب عليه الجهاد ولا تعطى له إلا في حال تلبسه بالغزو.</p>
