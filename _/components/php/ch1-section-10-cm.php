<p>كل ما ينقض الوضوء من الأحداث والأسباب المتقدمة فإنه ينقض التيمم أيضًا، ويزيد التيمم على الوضوء بأمر آخر لا ينقض  الوضوء وهو وجود الماء قبل الصلاة، فمن تيمم فوجد الماء قبل أن يصلي لزمه استعمال الماء وبطل عليه تيممه إن لم يضق  الوقت، فإن ضاق الوقت فلا يبطل تيممه، وأما من وجد الماء بعد الفراغ من الصلاة وكان خائفًا من لص أو سبع أو مترجيًا وقدم  الصلاة عن آخر الوقت المأمور بإيقاعها فيه وكان مقصرًا وهو قادر على استعمال الماء ولم يجد من يناوله إياه أو كان الماء في  رحله ونسيه فتيمم وصلى خوف خروج الوقت ثم وجده، أو كان مترددًا في لحوق الماء فقدم الصلاة ثم وجده فلا يبطل تيممه  وصلاته صحيحة ويعيد في الوقت المختار.</p>