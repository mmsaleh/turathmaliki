			<p>تكره الزيادة على ما فرضه وقدره فيه الشارع صلى الله عليه وسلم وهو المسح ورده في الرأس والمرة الواحدة في مسح الأذنين، وتكره أيضًا الزيادة على القدر الذي حدده الشارع في الغسل وهو الثلاث في اليدين والرجلين أو تمنع. ثم قال:</p>
				<table>
					<tr>
						<td>69</td>
						<td>وَعَاجِزُ الفَوْرِ بَنَى مَا لَمْ يَطُلْ</td>
						<td>بِيُبْسِ الَاعْضَا في زَمَانٍ مُعْتَدِلْ</td>
					</tr>
					<tr>
						<td>70</td>
						<td>ذَاكِرُ فَرْضِهِ بِطولٍ يَفْعَلُهْ</td>
						<td>فَقَطْ وَفي الْقُرْبِ المُوَالِي يُكْمِلُهْ</td>
					</tr>
				</table>
			<p>تقدم أن الفور وهو الموالاة من فرائض الوضوء، وأن المشهور وجوبه مع الذكر والقدرة وسقوطه مع العجز والنسيان، وأخبرهناأن من أخل به عاجزاً كمن أخذ من الماء ما يكفيه فأريق له في أثناء وضوئه ثم وجد ماء آخر لكمال طهارته فإن لم يجده إلا بعد طول من إراقة مائه بطل ما فعل من وضوئه وابتدأه من أوله، وإن وجد الماء بأثر إراقة مائه الأول فإنه يعتد بما فعل ويكمل وضوءه والطول هنا معتبر بالزمان الذي تجف فيه الأعضاء المعتدلة في الزمان المعتدل. وأما الناسي إذا فعل بعض الوضوء ونسي باقيه ثم تذكر فإنه يبني على ما فعل ويكمل ما بقي، ويجدد له النية، وسواء تذكر بالقرب أو بعد طول. ثم قال:</p>