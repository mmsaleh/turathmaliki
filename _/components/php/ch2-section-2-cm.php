<p> الحادية والعشرون: الأذان للجماعة الذين يطلبون غيرهم في الفرض الذي حضر وقته. </p>
<p>الثانية والعشرون: قصر الصلاة الرباعية، وهي الظهر والعصر والعشاء لمن سافر أربعة برد فأكثر فيصليها ركعتين ركعتين ولا يزال يقصر إلى أن يعود ويرجع من سفره ما لم ينو إقامة أربعة أيام صحيحة غير ملفقة، ويبتدئ القصر إذا جاوز المواضع المسكونة التي هي متصلة بالبلد، ولا يزال يقصر إلى أن يصل إلى ذلك الموضع في قدومه من سفره والبريد هو أربعة فراسخ، ففي أربعة برد ستة عشر فرسخًا والفرسخ ثلاثة أميال فحد مسافة القصر إذن بالزمان هو سفر يوم وليلة بسير الحيوانات المثقلة بالأحمال المعتادة، وهذا السفر يشترط فيه أن يكون مباحًا لا سفر معصية أو سفر لهو، ثم قال: </p>
