<p>حكم الجمعة الوجوب على الذكر الحر غير المعذور لمقيم ببلدها أو قرية أو خيم خارجة عنها قدر فرسخ. وشروط صحتها خمسة. الأول: الاستيطان ببلد مبني. الثاني: الجماعة الذين يدفعون عن أنفسهم الأمور الغالبة ولا يحدون بعدد، وتصح الجمعة بحضور اثنى عشر رجلاً باقين لسلامها. الثالث: الجامع ومن شرطه البنيان المخصوص على صفة المساجد المعتادة لأهل تلك البلدة. الرابع: الخطبة قبل الصلاة فإن جهل الإمام فصلى بلا خطبة خطب وأعاد الصلاة، ولو صلى ثم خطب أعاد الصلاة فقط ومن شرط الخطبة وصلها بالصلاة ولا يخطب إلا بعد الزوال، فإن خطب قبله أعاد الخطبة، وأول وقت الجمعة كالظهر وإيقاعها إثر الزوال أفضل، وآخر وقتها أن يبقى قدر ركعة واحدة بعد الفراغ منها للغروب، فإن لم يبق سقط وجوب الجمعة عنهم. الخامس: الإمام ويشترط كونه حراً مقيماً فلا تصح خلف إمام مسافر لم ينو إقامة أربعة أيام فأكثر فإن نواها ولزمته الجمعة بالتبع للمستوطنين فله أن يؤم فيها ولا تصح الجمعة خلف عبد وتجزئ الجمعة غير من تجب عليه عن الظهر. والذي لا تجب عليه: المسافر والمعذور بمرض يتعذر معه الإتيان أو لا يقدر عليه إلا بمشقة شديدة وتمريض القريب كالأب والولد سواء كان هناك ممرض أم لا أشرف على الموت أم لا، وتجزئ كذلك العبد والصبي والبعيد على أكثر من ثلاثة أميال والمرأة، فهؤلاء لا تجب عليهم الجمعة وإن صلوها أجزأتهم عن الظهر وحضورهم لها مستحب ومطلوب والسعي إليها أي الذهاب إليها في حق من تجب عليه واجد عند الأذان لها، وهذا في حق القريب، وأما البعيد فيجب عليه الذهاب قبل ذلك بمقدار ما يدركها. ثم قال: </p>
