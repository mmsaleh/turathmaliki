<p>الحج فرض على الإنسان مرة واحدة في عمره، وهو ثابت بالكتاب والسنة فمن جحد وجوبه فهو كافر مرتد، ومن أقر بوجوبه وتركه وكان مستطيعًا فالله حسيبه، وللحج شروط وجوب وشروط صحة، فشروط وجوبه: الحرّية والبلوغ والعقل والاستطاعة، فلا يجب على عبد ولا صغير ولا مجنون ولا على غير مستطيع، نعم يصح من الجميع ويقع نفلًا وشروط صحته الإسلام فقط والاستطاعة هي إمكان الوصول إلى مكة من عير مشقة عظيمة مع القدرة على أداء الصلوات في أوقاتها المشروعة في السفر وعدم الإخلال بشيء من فرائضها أو شروطها قال ((في المدخل))، (قال علماؤنا: إذا علم المكلف أنه تفوته صلاة واحدة إذا خرج إلى الحج سقط الحج) (ومن وجوه الاستطاعة) وجود الأمن على المال من لص أو مكاس وإلا لم يجب إلا أن يكون المكاس مسلماً يأخذ شيئاً لا يجحف بالشخص ولا يعود إلى الأخذ مرة ثانية، فإن علم أنه ينكث أو جهل حاله سقط الحج بلا خلاف. ثم اعلم أن الاستطاعة معدومة في المغرب ومن لا استطاعة له لا حج عليه هذا وأركان الحج التي هي فرائضه أربعة: أولها: الإحرام وهو نية أحد النسكين أو هما. الثاني: السعي بين الصفا والمروة. الثالث: الوقوف بعرفة ليلة عيد الأضحى. والرابع: طواف الإفاضة، وهذه الأركان الأربعة إن تركت كلها أو بعضها لا تجبر بالهدي والذي يجبر بالهدي هو الواجبات الآتي ذكرها ولاء. ثم قال:</p>
