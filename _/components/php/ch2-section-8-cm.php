<p>شروط الإمامة على قسمين شرط صحة وشرط كمال. فشرط الصحة هو إذا عدم بطلت الصلاة خلف ذلك الإمام وأعيدت أبدًا وشرط كمال هو إذا فقد فلا بأس لكن المطلوب هو وجوده، فأول شروط الصحة على ترتيب النظم أن يكون الإمام ذكرًا فمن صلّى خلف امرأة بطلت صلاته ويعيدها أبداً. الثاني: أن يكون مكلفًا عاقلًا بالغًا فمن ائتم بمجنون أو سكران غلب على عقله أو بصبي غير بالغ بطلت صلاته. الثالث: أن يكون قادرًا على أدائها والإتيان بأركانها من القيام والركوع والسجود فلا يصح ائتمام القادر على ذلك بالعاجز عنه. الرابع: أن يكون عارفًا بحكم الصلاة، أي عالماً بما لا تصح الصلاة إلا به من القراءة والفقه فلا تصح الصلاة خلف الإمام الأمّي الذي لا يحفظ من القرآن شيئًا ولا يعرفه. وأما الفقه فالمراد به معرفة كيفية الوضوء والغسل وأنه إن ترك لمعة بطل طهره وصلاته، وكذلك تعيين الصلاة التي شرع فيها. الخامس: كونه غير فاسق وهو شامل لفسق الجارحة كشرب الخمر وغيره ولفسق الاعتقاد كالقدري وغيره من أهل الأهواء، فمن صلّى خلف فاسق بوجهيه أعاد أبداً، وألحقوا بالفاسق المغتاب. أي الذي يغتاب الناس لا يصلى خلفه ابتداء، وإن صلى خلفه ففيه خلاف، قاله ابن ناجي في (شرح المدونة)، ونقله الشيخ الطالب في حاشيته. السادس: كونه غير لحان فلا تصح الصلاة خلف اللحان، قيل مطلقًا في الفاتحة وغيرها. وقيل في الفاتحة فقط، ومن اللحن عدم التمييز بين الضاد والظاء، السابع: كونه غير مقتد بغيره فمن ائتم بمأموم بطلت صلاته. </p>
