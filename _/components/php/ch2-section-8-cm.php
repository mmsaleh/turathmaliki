<p>هذا شروع من الناظم في عد شروط الكمال الأحد عشر، والإمامة مع هذه الأوصاف صحيحة لكن الأولى سلامة الإمام منها، واتصافه بشيء منها مكروه. </p>
<p>أولها: إمامة صاحب السلس والقروح للسالم من ذلك بناء على أن الرخصة لا تتعدى محلها.</p>
<p>الثاني: إماماة الرجل من أهل البادية للحاضرين.</p>
<p>الثالث: إمامة من تكرهه الجماعة ذوو الفضل لا مطلق الناس فمن علم أن جماعة من ذوي الفضل كارهون لإمامته وجب عليه أن يتأخر عن الإمامة بهم.</p>
<p>الرابع: إمامة الأشلّ وهو يابس اليد لجرح أو غيره، وكذا أقطع اليد وشبهه، وتجوز إمامة الأعرج إذا كان عرجه خفيفًا وغيره أولى،</p> 
<p>الخامس: الإمامة في المسجد بلا رداء وأما في غيره فلا كراهة، ويكفي عن الرداء الحائك لأنه فيه ما في الرداء وزيادة، ولذلك استمر عمل الأئمة المقتدى بهم علماً وديناً على ذلك، وأما لبس الإمام اليوم للسلهام والجلابية من غير الرداء مع تغطية الرأس فالظاهر أنه ينظر في كل موضوع بخصوص فمن هو عندهم حسن الهيئة ويلبسونه بالمحافل تنزل منزلة الرداء في حقهم وإلا فلا، ثم استطرد النظم أثناء شروط الكمال ثلاثة فروع من فروع الصلاة مع الجماعة لمشاركتها مع ما قبلها في الحكم وهو الكراهة فقال: </p>
<p>صلاة تجتلى بين الأساطين الخ</p>
<p>فأولها: الصلاة بين الأساطين، أي بين السواري لكن مع الاختيار، وعلة الكراهة تقطيع الصفوف.</p> 
<p>ثانيها: صلاة المأموم أمام إمامه خوف أن يطرأ على الإمام ما لا يعلمونه مما يبطلها وقد يخطئون في ترتيب الركعات إذا تقدموه، ومحل الكراهة عند عدم الضرورة، وأما لضيق المسجد فلا بأس بذلك. </p>
<p>ثالثها: إعادة الجماعة بعد الإمام الراتب فإعادة جماعة بإمام بعد صلاة الإمام الراتب مكروهة لأن ذلك يؤدي إلى تفريق الجماعة، والشارع  (  أمر بالألفة، ومحل الكراهة إن صلى الإمام في وقته المعتاد وأما إن قدم أو أخر وتضرر الناس بانتظاره فيجوز لغيره الجمع بعده وقبله ولم يجمع هو إن جاء بعد الوقت وقد جمعوا،</p>
<p>السادس: من شروط كمال الإمامة عدم اتخاذ من جهل حاله في العدالة أو في الفسق إمامًا راتبًا أما مطلق إمامته من غير أن يتخذ إمامًا راتبًا فجائز، </p>
<p>السابع: اتخاذ المأبون المتهم بذلك بعدما تاب وحسنت حالته إمامًا راتبًا.</p>
<p>الثامن: اتخاذ الأغلف وهو الذي لم يختتن إمامًا راتبًا، والاختتان في مذهبنا المالكي سنة، وفي غيره فرض كمذهب الشافعي.</p>
<p>التاسع: اتخاذ العبد إمامًا راتبًا.</p>
<p>العاشر: اتخاذ الخصيّ إمامًا راتبًا وهو الذي قطع ذكره فقط أو أنثياه، أما مقطوعهما معًا فهو المجبوب.</p>
<p>الحادي عشر: اتخاذ ولد الزنا إمامًا راتبًا خوف أن يعرض نفسه للقول فيه لأن إمامته موضع رفعة وكمال يتنافس فيها ويحسد عليها وهذا وجه كراهة ترتب هؤلاء للإمامة وهو سرعة الألسنة إليهم وربما تعدى إلى من ائتم بهم. ثم قال: </p>
