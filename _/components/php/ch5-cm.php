		<p>ينبغي للحاج أن يرجع يوم العيد من مكة إلى منى، والأفضل أن يصلي بها الظهر إن أمكنه ذلك، ويقيم بها بقية يوم النحر وثلاثة أيام بعده لرمي الجمار، والمبيت بها واجب ثلاث ليال لمن لم يتعجل وليلتين للمتعجل، فإن تركه رأساً أوجل ليلة فقط فالدم، فإذا زالت الشمس من اليوم الثاني فليذهب ماشياً متوضئاً قبل صلاة الظهر ومعه إحدى وعشرون حصاة فيبتدئ بالجمرة الأولى وهي التي تلي مسجد منى فيرميها وهو مستقبل مكة بسبع حصيات ويكبر مع كل حصاة، ثم يتقدم أمامها وهو مستقبل القبلة، ثم يدعو ويمكث في الدعاء قدر إسراع سورة البقرة، ثم يأتي الجمرة الوسطى فيرميها بسبع حصيات أيضًا، ثم يتقدم أمامها ذات الشمال ويجعلها على يمينه ويدعو قدر إسراع سورة البقرة أيضًا، ثم يأتي جمرة العقبة فيرميها بسبع حصيات لا يقف عندها لضيق موضعها، فإذا زالت الشمس من اليوم الثالث من يوم النحر رمى الجمار الثلاث على الصفة المتقدمة، ثم إن شاء أن يتعجل إلى مكة فله ذلك ويسقط عنه المبيت ليلة الرابع ورمي يومها، ويشترط في صحة التعجيل أن يخرج من منى قبل غروب الشمس من اليوم الثالث، وإن غربت قبل أن يجاوز جمرة العقبة لزمه المبيت بمنى ورمي اليوم الرابع، فإذا زالت الشمس في اليوم الرابع ورمى الجمار الثلاث كما تقدم وقد تم حجه فلينفر من منى فإذا وصل للأبطح نزل به استحباباً فصلى به الظهر والعصر والمغرب والعشاء ويقصر الرباعية وما خاف خروج وقته قبل الوصول للأبطح صلاه حيث كان، فإذا صلى العشاء قدم إلى مكة. ويستحب له الإكثار من الطواف ما دام بها، ومن شرب ماء زمزم والوضوء به وملازمة الصلاة في الجماعة الأولى، ثم قال:</p>
