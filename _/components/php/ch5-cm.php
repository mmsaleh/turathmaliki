<p>الأفعال الواجبة التي ليست بأركان تنجبر بالدم وهو الهدي، بمعنى أن من ترك واحداً منها فعليه الدم، وذلك بدنة أو بقرة أو شاة يذبحها أو ينحرها للمساكين، وهي على ما ذكر الناظم أحد عشر فعلاً: منها: طواف القدوم فمن تركه عامداً مختاراً سواء داخل مكة أم لا بأن مضى إلى عرفات بعد إحرامه من الميقات فعليه الدم ما لم يخف فوات الوقوف، فحينئذ لا يجب عليه طواف القدوم ولا دم عليه في تركه، وكذلك إن تركه ناسياً، ومنها وصل طواف القدوم بالسعي بين الصفا والمروة فإن لم يصله به إما بأن ترك السعي بعده رأساً أو سعى بعد طول فعليه الدم أيضاً. ومنها المشي في الطواف والسعي فإن ركب لغير ضرورة فإنه يعيد إن قرب، فإن فات أهدى، فإن ركب لعجز جاز ومنها ركعتا الطواف الواجب وهو طواف القدوم وطواف الإفاضة فإذا ترك الركوع بعد هذين الطوافين وبعد عن مكة فعليه الهدي ولو تركها نسياناً ومنها النزول بالمزدلفة في الرجوع من عرفة ليلة النحر ولا يكفي في النزول إناخة البعير بل لا بد له من حط الرحال فمن تركه فعليه الدم، ومنها المبيت بمنى ثلاث ليال لرمي الجمار وهي الليالي التي بعد عرفة تركها رأساً أو ليلة واحدة بل أوجل ليلة فعليه الدم وأما الليلي التي قبل عرفة فلا دم في تركها، ومنها الإحرام من الميقات فمن جاوزه حالًا وهو قاصد الحج أو عمرة فقد أساء، فإن أحرم بعد مجاوزته فعليه الدم، ومنها التجرد من مخيط الثياب فإن تركه ولبس المخيط لغير عذر فعليه الدم وهذا خاص بالرجال دون المرأة، ومنها التلبية إذا تركها بالكلية أو تركها أول الإحرام حتى طال أو فعلها أول الإحرام ثم تركها في بقيته فعليه الدم، ومنها الحلاق، فإذا تركه حتى رجع إلى بلده أو طاف فعليه الدم ومنها رمي الجمار فيجب الدم في تركه رأسًا أو ترك جمرة واحدة من الجمار الثلاث أو في ترك حصاة من جمرة منها إلى الليل ورمي الجمار هو آخر الأفعال الواجبة في الحج، ولما عد الناظم الإحرام من الميقات من جملة هذه الأفعال المنجبرة بالدم استطرد ببيان الميقات المكاني أي المكان الذي يتعين على الحاج الإحرام منه وذلك يختلف باختلاف بلدة المحرم فأخبر أن ذا الحليفة ميقات أهل طيبة وهي المدينة المشرفة على ساكنها أفضل الصلاة والسلام، وميقات لمن مر بها من غير أهلها، وأن الجحفة ميقات لأهل الشام وأهل مصر وأحل المغرب والروم ولمن مر عليها من عير أهلها، وأن قرناً ميقات لأهل نجد اليمن ونجد الحجاز ولمن مر به من غير أهلها، وأن ذات عرق ميقات لأهل العراق وفارس وخرسان والمشرق ولمن مر به من غير أهله، وأن يلملم ميقات لأهل اليمن والهند ويماني تهامة ولمن مر به من غير أهله، ثم قال:</p>
